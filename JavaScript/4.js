// FUNCTION
// A sets of instruction that perform a task.

// 1. Function Declaration
function loki() {
    const a = 10;
    const b = 20;
    const c = a + b;
    console.log(c);
}

loki();

function bijay(a, b) {
    const c = a - b;
    console.log(c);
}

bijay(100, 50);
bijay(1000, 100);

// Arrow function
const lokichaulagain = () => {
    const b = 1;
    const c = 2;
    const d = b + c;
    console.log(d);
};
lokichaulagain();

const bijaydevkota = (a, b) => {
    const c = a * b;
    console.log(c);
};
bijaydevkota(10, 10)